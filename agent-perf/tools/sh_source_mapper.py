"""Map between JS source, generated C, and native code for Static Hermes.

Parses #line directives in C code generated by `shermes -emit-c` to build
a mapping from C lines back to original JavaScript source lines.  This
enables correlating hot spots in the generated native code with the original
JS source.

The tool can:
    - Build and dump the full source map
    - Query which JS line corresponds to a given C line
    - Query which C lines correspond to a given JS line

Usage:
    python3 sh_source_mapper.py --input output.c [--json]
    python3 sh_source_mapper.py --input output.c --c-line 42
    python3 sh_source_mapper.py --input output.c --js-line 10

Examples:
    shermes -emit-c -o output.c input.js
    python3 sh_source_mapper.py --input output.c --c-line 150
    python3 sh_source_mapper.py --input output.c --js-line 25 --json
    python3 sh_source_mapper.py --input output.c --json
"""

import argparse
import json
import re
from typing import Dict, List, Optional


def parse_line_directives(source: str) -> List[Dict]:
    """Parse #line directives from generated C code.

    #line directives have the form:
        #line 42 "input.js"
    or in some compilers:
        # 42 "input.js"

    Returns a list of mapping entries, each with:
        {c_line, js_line, js_file, c_source}
    where c_line is the C file line number (1-based) of the code following
    the directive.
    """
    lines = source.split("\n")
    mappings: List[Dict] = []

    # Current JS file and line context from most recent #line directive.
    current_js_file: Optional[str] = None
    current_js_line: Optional[int] = None

    # Regex for #line directives.
    line_directive = re.compile(r"^\s*#\s*line\s+(\d+)\s+\"([^\"]+)\"")
    # Alternate form: # 42 "file.js"
    alt_directive = re.compile(r"^\s*#\s*(\d+)\s+\"([^\"]+)\"")

    for i, line in enumerate(lines):
        lineno = i + 1  # 1-based.

        m = line_directive.match(line)
        if not m:
            m = alt_directive.match(line)
        if m:
            current_js_line = int(m.group(1))
            current_js_file = m.group(2)
            continue

        if current_js_file is not None and current_js_line is not None:
            stripped = line.strip()
            # Skip empty lines and pure braces.
            if stripped and stripped not in ("{", "}"):
                mappings.append(
                    {
                        "c_line": lineno,
                        "js_line": current_js_line,
                        "js_file": current_js_file,
                        "c_source": stripped,
                    }
                )
                current_js_line += 1

    return mappings


def build_c_to_js_map(mappings: List[Dict]) -> Dict[int, Dict]:
    """Build a lookup from C line number to JS source info."""
    return {m["c_line"]: m for m in mappings}


def build_js_to_c_map(mappings: List[Dict]) -> Dict[str, Dict[int, List[int]]]:
    """Build a lookup from (JS file, JS line) to list of C line numbers."""
    result: Dict[str, Dict[int, List[int]]] = {}
    for m in mappings:
        js_file = m["js_file"]
        if js_file not in result:
            result[js_file] = {}
        js_line = m["js_line"]
        if js_line not in result[js_file]:
            result[js_file][js_line] = []
        result[js_file][js_line].append(m["c_line"])
    return result


def query_c_line(mappings: List[Dict], c_line: int) -> Optional[Dict]:
    """Find the JS source mapping for a given C line number.

    If there is no exact match, returns the nearest preceding mapping.
    """
    c_to_js = build_c_to_js_map(mappings)
    if c_line in c_to_js:
        return c_to_js[c_line]

    # Find nearest preceding mapping.
    best: Optional[Dict] = None
    for m in mappings:
        if m["c_line"] <= c_line:
            if best is None or m["c_line"] > best["c_line"]:
                best = m
    if best:
        # Adjust js_line based on offset from the matched C line.
        offset = c_line - best["c_line"]
        return {
            "c_line": c_line,
            "js_line": best["js_line"] + offset,
            "js_file": best["js_file"],
            "c_source": "(approximate)",
            "approximate": True,
        }
    return None


def query_js_line(
    mappings: List[Dict], js_line: int, js_file: Optional[str] = None
) -> List[Dict]:
    """Find all C lines that map to a given JS source line."""
    results = []
    for m in mappings:
        if m["js_line"] == js_line:
            if js_file is None or js_file in m["js_file"]:
                results.append(m)
    return results


def format_human_full(mappings: List[Dict]) -> str:
    """Format the full source map as a human-readable table."""
    if not mappings:
        return "No #line directives found in input."

    lines: List[str] = []
    lines.append(f"Source map: {len(mappings)} entries")
    lines.append("")
    lines.append(f"  {'C Line':>7s}  {'JS File':<30s}  {'JS Line':>7s}  C Source")
    lines.append("  " + "-" * 80)

    for m in mappings[:100]:
        lines.append(
            f"  {m['c_line']:>7d}  "
            f"{m['js_file']:<30s}  "
            f"{m['js_line']:>7d}  "
            f"{m['c_source'][:60]}"
        )
    if len(mappings) > 100:
        lines.append(f"  ... and {len(mappings) - 100} more entries")

    return "\n".join(lines)


def format_human_query(result: Dict, direction: str) -> str:
    """Format a single query result."""
    if not result:
        return "No mapping found."
    approx = " (approximate)" if result.get("approximate") else ""
    return (
        f"C line {result['c_line']} -> "
        f"{result['js_file']}:{result['js_line']}{approx}\n"
        f"  C source: {result['c_source']}"
    )


def main() -> None:
    parser = argparse.ArgumentParser(
        description=(
            "Map between JS source and generated C code using #line "
            "directives from shermes -emit-c output."
        ),
    )
    parser.add_argument(
        "--input",
        "-i",
        type=str,
        required=True,
        help="Path to the .c file generated by shermes -emit-c",
    )
    parser.add_argument(
        "--c-line",
        type=int,
        default=None,
        help="Query: find the JS source line for this C line number",
    )
    parser.add_argument(
        "--js-line",
        type=int,
        default=None,
        help="Query: find all C lines mapping to this JS source line",
    )
    parser.add_argument(
        "--js-file",
        type=str,
        default=None,
        help="Filter --js-line results to a specific JS file (substring match)",
    )
    parser.add_argument(
        "--json",
        action="store_true",
        help="Output results as JSON (default: human-readable)",
    )
    args = parser.parse_args()

    with open(args.input, "r") as f:
        source = f.read()

    mappings = parse_line_directives(source)

    if args.c_line is not None:
        result = query_c_line(mappings, args.c_line)
        if args.json:
            print(json.dumps(result))
        else:
            if result:
                print(format_human_query(result, "c_to_js"))
            else:
                print(f"No mapping found for C line {args.c_line}")

    elif args.js_line is not None:
        results = query_js_line(mappings, args.js_line, args.js_file)
        if args.json:
            print(json.dumps(results))
        else:
            if results:
                print(f"JS line {args.js_line} maps to {len(results)} C line(s):")
                for r in results:
                    print(f"  C line {r['c_line']}: {r['c_source'][:70]}")
            else:
                print(f"No C lines found for JS line {args.js_line}")

    else:
        # Dump full source map.
        if args.json:
            print(json.dumps(mappings))
        else:
            print(format_human_full(mappings))


if __name__ == "__main__":
    main()
